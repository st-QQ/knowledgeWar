/**
 * 根据级别从成语数组中随机获取一个成语
 * @param {string} level - 成语的级别
 * @returns {string} - 随机获取的成语
 */
export function getRandomIdiom(level) {
    const idioms = idiomLevels[level];
    if (!idioms) {
        throw new Error(`未找到级别为 ${level} 的成语`);
    }
    const randomIndex = Math.floor(Math.random() * idioms.length);
    return idioms[randomIndex];
}
    